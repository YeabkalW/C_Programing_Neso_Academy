#include <stdio.h>

/**
 * right shift bitwise operator (>>): shifts bits to the right
 * when using right shift bitwise operator we are dividing the 
 * left operand by 2 the raised the power of right operand
 * 
 * main - shows how right shift bitwise operator works
 * 
 * return: always 0. 
 * 
 */

int main(void)
{
    int var = 45;

    printf("%d\n", var >> 10);

    return 0;

}
